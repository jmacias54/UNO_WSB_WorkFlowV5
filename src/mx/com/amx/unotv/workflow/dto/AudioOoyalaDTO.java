package mx.com.amx.unotv.workflow.dto;

public class AudioOoyalaDTO {

	private String url;
	private String alternate_text;
	private String duration;
	private String file_size;
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getAlternate_text() {
		return alternate_text;
	}
	public void setAlternate_text(String alternate_text) {
		this.alternate_text = alternate_text;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	
	
}
