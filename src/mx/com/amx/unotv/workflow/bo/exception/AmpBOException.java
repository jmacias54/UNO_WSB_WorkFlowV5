package mx.com.amx.unotv.workflow.bo.exception;

public class AmpBOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AmpBOException() {
		super();
	}

	public AmpBOException(String message, Throwable cause) {
		super(message, cause);
	}

	public AmpBOException(String message) {
		super(message);
	}

	public AmpBOException(Throwable cause) {
		super(cause);
	}
}
